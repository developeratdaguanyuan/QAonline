package doraemon.mongoDBDataStructure;

public class MidNameData {
  private String mid;
  private String name;
  
  public void setMid(String m) {
    mid = m;
  }
  
  public void setName(String n) {
    name = n;
  }
  
  public String getMid() {
    return mid;
  }
  
  public String getName() {
    return name;
  }
}
